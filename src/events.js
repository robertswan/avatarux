//------------------------------------------------------------------------------
function Events () {
    const self = this;

    this.queue = [];

    //------------------------------------------------------------------------------
    this.push = (event) => {
        self.queue.push (event);
    };

    //------------------------------------------------------------------------------
    this.hasPending = () => self.queue.length > 0;

    //------------------------------------------------------------------------------
    this.pop = () => {
        console.assert (self.hasPending ());
        return self.queue.shift ();
    };
}

//------------------------------------------------------------------------------
module.exports = Events;
